export function getQuizResult(avgScore) {
  if (avgScore < 30) {
    return {
      title: 'Низкий риск автоматизации',
      percent: Math.round(avgScore),
      text: 'Ваша профессия сильно опирается на человеческие навыки. ИИ скорее станет помощником, чем заменой.',
      tips: [
        'Используйте ИИ для рутины, чтобы освободить время на творчество.',
        'Развивайте экспертизу и коммуникацию.',
      ],
    }
  }
  if (avgScore < 55) {
    return {
      title: 'Умеренный риск',
      percent: Math.round(avgScore),
      text: 'Часть задач может автоматизироваться. Важно учиться работать вместе с ИИ.',
      tips: [
        'Освойте 1–2 ИИ-инструмента в своей сфере.',
        'Углубляйте навыки, которые сложно заменить.',
      ],
    }
  }
  if (avgScore < 75) {
    return {
      title: 'Заметный риск изменений',
      percent: Math.round(avgScore),
      text: 'Многие процессы в профессии могут измениться. Нужна переквалификация и цифровая грамотность.',
      tips: [
        'Изучите смежные роли с ИИ (аналитика, автоматизация).',
        'Пройдите курсы по данным или prompt-инженерии.',
      ],
    }
  }
  return {
    title: 'Высокий риск автоматизации',
    percent: Math.round(Math.min(avgScore, 92)),
    text: 'Рутинные функции с высокой вероятностью перейдут к ИИ. Стоит заранее менять фокус навыков.',
    tips: [
      'Перейдите к задачам с контролем, творчеством и работой с людьми.',
      'Начните учиться управлять ИИ-системами, а не конкурировать с ними.',
    ],
  }
}
